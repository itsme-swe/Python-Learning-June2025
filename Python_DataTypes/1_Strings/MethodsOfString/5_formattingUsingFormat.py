print("This site is {:.6f}% securely {}!!".format(100, "encrypted"))

# 1️⃣ Limiting precision
print(
    "My average of this {} was {:.2f}%".format("semester", 78.234876)
)  # My average of this semester was 78.23%

# 2️⃣ # Convert an integer to its binary or other bases
print("The {} of 100 is {:b}".format("binary", 100))  # The binary of 100 is 1100100
